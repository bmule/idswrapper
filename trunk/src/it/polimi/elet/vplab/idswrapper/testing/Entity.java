package it.polimi.elet.vplab.idswrapper.testing;

public class Entity {
	protected String id;
	protected String name;
	
	public Entity() {
		super();
	}
	
	public String getID() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setID(String id) {
		this.id = id;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
